	MOVWF PORTB
	
	MOVLW 0x66
	MOVWF PORTB
	
	MOVLW 0x6D
	MOVWF PORTB
	
	MOVLW 0x7D
	MOVWF PORTB
	
	MOVLW 0x07
	MOVWF PORTB
	
	MOVLW 0x7F
	MOVWF PORTB
	
	MOVLW 0x6F
	MOVWF PORTB
	
	MOVLW 0x77
	MOVWF PORTB
	
	MOVLW 0x7C
	MOVWF PORTB
	
	MOVLW 0x39
	MOVWF PORTB
	
